@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        name_data = form.name.data
        email_data = form.email.data
        password_data = form.password.data

        if User.query.filter_by(email=email_data).first():
            # User already exists
            flash("You've already signed up with that email, log in instead!")
            # redirect to home
            return redirect(url_for('login'))

        hash_and_salted_password = generate_password_hash(
            password=password_data,
            method="pbkdf2:sha256",
            salt_length=8
        )

        new_user = User(
            email=email_data,
            name=name_data,
            password=hash_and_salted_password
        )
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email_data = form.email.data
        password_data = form.password.data

        user = User.query.filter_by(email=email_data).first()
        # Email doesn't exist
        if not user:
            flash("That email does not exist, please try again.")
            return redirect(url_for('login'))
        # Password incorrect
        elif not check_password_hash(user.password, password_data):
            flash('Password incorrect, please try again.')
            return redirect(url_for('login'))
        # Email exists and password correct
        else:
            login_user(user)
            return redirect(url_for("get_all_posts"))

    return render_template("login.html", form=form)


